<template>
  <div class="panel">
    <my-profile-setting 
      :coming-soon="false" 
      setting="password"
      title="Account Password" 
      description="Change your password" 
      button="Change Password"
      @activateModal="showModal"/>
    <MyProfileModal v-bind="{fetchRequest}" @userData="emitNewData"/>
  </div>
</template>

<script>
import MyProfileSetting from "~/components/MyProfileSetting.vue";
import MyProfileModal from "~/components/myProfile/MyProfileModal.vue";

export default {
  components: {
    MyProfileSetting,
    MyProfileModal
  },
  props: {
    userInfo: {
      type: String,
      default: undefined
    }
  },
  data: function() {
    return {
      async fetchRequest(insertData) {
        const result = await this.$axios.$put("/manage/change-password/", {
          password: insertData
        });
        return result;
      },
      loadModal: false
    };
  },
  methods: {
    showModal(setting) {
      if (setting === "password") {
        this.$modal.show("MyProfileModal", {
          request: "Password",
          currentData: "",
          fetchRequest: this.fetchRequest
        });
        this.loadModal = true;
      }
    },
    emitNewData(data) {
      data.then(result => {
        this.$emit("newUserData", result);
      });
    }
  }
};
</script>
